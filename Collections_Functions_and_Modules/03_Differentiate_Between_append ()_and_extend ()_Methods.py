# Differentiate between append () and extend () methods?

#append :- append function in python adds a single element to the end of thelist.
#Extend :- extend function adds multiple elements to the list.


# append ex:-

l = [1,2,3]
l.append(4)
print(l)



# extend Ex:-

l=[11,12,13,14]
l.extend([15,16])
print(l)
